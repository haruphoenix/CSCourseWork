#include <iostream>
using namespace std;

/**************************************************************************************
* calculates e
***************************************************************************************/
int main()
{
   long double e = 0.0;
   long double m = 1.0;
   for (float i = 1.0; i <= 200.0; i++)
      {
         m = 1.0;
         for (float x = 1.0; x < i; x++)
            {
               m *= x;
            }
         e += 1/m;
      }
   cout.setf(ios::fixed);
   cout.setf(ios::showpoint);
   cout.precision(62);
   cout << e << endl;
   return 0;
}
